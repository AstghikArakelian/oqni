  uint32_t ppg;
  int setup = SETUP_DONE;
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	if(Command_Check() == COMMAND_RECEIVED)
	{
		SM_Case = ST_IDLE;
	}
	switch(SM_Case)
	{
		case ST_IDLE:
			if (command == COMMAND_START)
			{
				SM_Case = ST_START;
			}
			if (command == COMMAND_STOP)
			{
				SM_Case = ST_STOP;
			}
			break;
		case ST_START:
			if (setup == SETUP_NOTDONE)
			{
				ob1203_send_preambula();
				OB1203_Setup();
				setup = SETUP_DONE;
			}
			if(adc_rdy)
			{
				memset(&ppg, 0, sizeof(ppg));
				heartrate11_read_fifo(0, &ppg);
				ob1203_send_results(ppg);
				adc_rdy = 0;
			}
			break;
		case ST_STOP:
			OB1203_RST();
			setup = SETUP_NOTDONE;
			break;

	}
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
